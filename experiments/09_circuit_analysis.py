def create_per_layer_discriminative_plot(
    per_layer_top: Dict[int, List[FeatureNode]],
    raw_stats: Dict[Tuple[int, int], Dict],
    feature_labels: Dict[int, Dict[int, str]],
    title: str,
    save_path: Path,
    top_per_layer: int = 3,
):
    """Create a plot showing top discriminative features for each layer."""
    layers = sorted(per_layer_top.keys())
    if not layers:
        print(f"Warning: No per-layer features to plot")
        return

    n_layers = len(layers)
    fig_height = max(16, n_layers * 1.0)
    fig, axes = plt.subplots(1, 2, figsize=(24, fig_height))

    y_labels = []
    cohens_d_values = []
    consistency_values = []
    colors_d = []
    y_positions = []

    y_pos = 0
    for layer in layers:
        layer_feats = per_layer_top.get(layer, [])[:top_per_layer]
        if not layer_feats:
            continue
        for feat in reversed(layer_feats):
            key = (feat.layer, feat.feature_idx)
            stats = raw_stats.get(key, {})
            cohens_d = stats.get('cohens_d', feat.differential_effect)
            consistency = stats.get('consistency', 0)

            label = feature_labels.get(feat.layer, {}).get(feat.feature_idx, "")
            if not label:
                feat_label = f"L{feat.layer}.F{feat.feature_idx}"
            else:
                feat_label = f"L{feat.layer}.F{feat.feature_idx}: {label[:40]}"

            y_labels.append(feat_label)
            cohens_d_values.append(cohens_d)
            consistency_values.append(consistency)
            colors_d.append('#28a745' if cohens_d > 0 else '#dc3545')
            y_positions.append(y_pos)
            y_pos += 1
        y_pos += 0.5  # gap between layers

    ax1 = axes[0]
    ax1.barh(y_positions, cohens_d_values, color=colors_d, alpha=0.8, height=0.8)
    ax1.set_yticks(y_positions)
    ax1.set_yticklabels(y_labels, fontsize=9, fontfamily='monospace')
    ax1.set_xlabel("Cohen's d (effect size)", fontsize=12)
    ax1.set_title("Cohen's d: SUCCESS-aligned (green) vs FAILURE-aligned (red)",
                  fontsize=12, fontweight='bold')
    ax1.axvline(x=0, color='black', linewidth=0.5)
    ax1.grid(axis='x', alpha=0.3)

    ax2 = axes[1]
    ax2.barh(y_positions, consistency_values, color='steelblue', alpha=0.8, height=0.8)
    ax2.set_yticks(y_positions)
    ax2.set_yticklabels(y_labels, fontsize=9, fontfamily='monospace')
    ax2.set_xlabel('Consistency (fraction of concepts)', fontsize=12)
    ax2.set_title('Cross-Concept Consistency', fontsize=12, fontweight='bold')
    ax2.set_xlim(0, 1.05)
    ax2.axvline(x=0.5, color='gray', linewidth=0.5, linestyle='--')
    ax2.grid(axis='x', alpha=0.3)

    fig.suptitle(title, fontsize=14, fontweight='bold', y=1.01)
    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight', facecolor='white')
    print(f"Saved: {save_path}")
    plt.close()


def create_aggregated_circuit_plot(
    circuits: Dict[str, AggregatedCircuit],
    feature_labels: Dict[int, Dict[int, str]],
    title: str,
    save_path: Path,
):
    """Create comparison plot of circuits with arrows between layers."""
    n_conditions = len(circuits)
    fig, axes = plt.subplots(1, n_conditions, figsize=(16*n_conditions, 24))

    if n_conditions == 1:
        axes = [axes]

    for ax_idx, (condition, circuit) in enumerate(circuits.items()):
        ax = axes[ax_idx]

        if not circuit.features:
            ax.text(0.5, 0.5, f"No features for {condition}",
                   ha='center', va='center', fontsize=14)
            ax.set_title(f"{condition.upper()}", fontsize=16, fontweight='bold')
            ax.axis('off')
            continue

        layers_to_features = defaultdict(list)
        for feat in circuit.features:
            layers_to_features[feat.layer].append(feat)

        sorted_layers = sorted(layers_to_features.keys())
        n_layers = len(sorted_layers)

        ax.set_xlim(-1, 18)
        ax.set_ylim(-1, n_layers * 2.2 + 1)
        ax.axis('off')

        node_width = 5.0
        node_height = 1.6
        y_spacing = 2.2
        layer_node_positions = {}

        for layer_idx, layer in enumerate(sorted_layers):
            features = layers_to_features[layer][:3]
            y = layer_idx * y_spacing

            total_width = len(features) * node_width + (len(features) - 1) * 0.5
            start_x = 8.5 - total_width / 2
            layer_node_positions[layer] = []

            for feat_idx, feat in enumerate(features):
                x = start_x + feat_idx * (node_width + 0.5)

                if feat.differential_effect > 20:
                    color = '#28a745'
                elif feat.differential_effect < -20:
                    color = '#dc3545'
                else:
                    color = '#6c757d'

                rect = FancyBboxPatch(
                    (x, y - node_height/2), node_width, node_height,
                    boxstyle="round,pad=0.03,rounding_size=0.15",
                    facecolor=color, edgecolor='#343a40', linewidth=2
                )
                ax.add_patch(rect)

                center_x = x + node_width/2
                center_y = y
                layer_node_positions[layer].append((center_x, center_y))

                feature_id = f"L{layer}F{feat.feature_idx}"
                label = feature_labels.get(feat.layer, {}).get(feat.feature_idx, "")
                if label:
                    words = label.split()
                    lines = []
                    current_line = []
                    current_len = 0
                    for word in words:
                        if current_len + len(word) + 1 <= 25:
                            current_line.append(word)
                            current_len += len(word) + 1
                        else:
                            if current_line:
                                lines.append(' '.join(current_line))
                            current_line = [word]
                            current_len = len(word)
                    if current_line:
                        lines.append(' '.join(current_line))
                    if len(lines) > 3:
                        lines = lines[:3]
                    display_label = '\n'.join(lines)
                else:
                    display_label = ""

                ax.text(center_x, y + 0.5, feature_id,
                       ha='center', va='center', fontsize=11,
                       fontweight='bold', color='white')
                if display_label:
                    ax.text(center_x, y, display_label,
                           ha='center', va='center', fontsize=9,
                           color='white', linespacing=0.9)
                ax.text(center_x, y - 0.55, f"act={feat.differential_effect:.0f}",
                       ha='center', va='center', fontsize=10,
                       color='white', fontweight='bold')

            ax.text(-0.5, y, f"L{layer}", ha='right', va='center',
                   fontsize=10, fontweight='bold', color='#495057')

        # Draw arrows between consecutive layers
        for i in range(len(sorted_layers) - 1):
            layer_curr = sorted_layers[i]
            layer_next = sorted_layers[i + 1]
            curr_positions = layer_node_positions[layer_curr]
            next_positions = layer_node_positions[layer_next]
            next_center_x = sum(p[0] for p in next_positions) / len(next_positions)
            next_y = next_positions[0][1] - node_height/2 - 0.1
            for (cx, cy) in curr_positions:
                start_y = cy + node_height/2 + 0.05
                ax.annotate('', xy=(next_center_x, next_y), xytext=(cx, start_y),
                           arrowprops=dict(arrowstyle='->', color='#adb5bd', lw=1.5,
                                          connectionstyle='arc3,rad=0.1'))

        yes_pct = circuit.mean_yes_prob * 100
        no_pct = circuit.mean_no_prob * 100
        ax.set_title(
            f"{condition.upper()}\nYES: {yes_pct:.2f}%, NO: {no_pct:.2f}%\n"
            f"({circuit.n_concepts} concepts x {circuit.n_trials} trials)",
            fontsize=14, fontweight='bold', pad=10
        )

    fig.suptitle(title, fontsize=16, fontweight='bold', y=0.99)
    plt.tight_layout(rect=[0, 0, 1, 0.97])
    plt.savefig(save_path, dpi=150, bbox_inches='tight', facecolor='white')
    print(f"Saved: {save_path}")
    plt.close()


