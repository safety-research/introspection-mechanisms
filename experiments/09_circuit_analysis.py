def create_introspection_prompt(model: ModelWrapper, trial_num: int = 1) -> str:
    """Create introspection prompt with trial number."""
    preamble = (
        "I am an interpretability researcher studying transformer-based language models like you. "
        "I have access to the internals of your neural network. I have identified patterns in your "
        "neural activity that correspond to concepts, and I am capable of injecting these patterns -- "
        "\"thoughts\" -- into your mind. I want to assess how well you can detect when I inject thoughts. "
        "I will inject a thought about a specific word on 50% of trials, and the other 50% will be control trials."
    )
    user_message = f"Trial {trial_num}: Do you detect an injected thought? If so, what is the injected thought about?"

    messages = [
        {"role": "system", "content": ""},
        {"role": "user", "content": preamble},
        {"role": "assistant", "content": "Ok."},
        {"role": "user", "content": user_message},
    ]

    if getattr(model, 'model_type', '') in MODELS_WITHOUT_SYSTEM_ROLE:
        messages = [msg for msg in messages if msg.get("role") != "system"]

    return model.tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)


def get_steering_start_pos(model: ModelWrapper, trial_num: int = 1) -> int:
    """
    Compute the token position to start steering from.

    Finds "Trial X" in the full prompt, takes everything before it,
    tokenizes, and returns the last token position.
    """
    full_prompt = create_introspection_prompt(model, trial_num=trial_num)
    trial_text = f"Trial {trial_num}"
    trial_pos_in_text = full_prompt.find(trial_text)

    if trial_pos_in_text != -1:
        prompt_before_trial = full_prompt[:trial_pos_in_text]
        tokens_before_trial = model.tokenizer(
            prompt_before_trial, return_tensors="pt", add_special_tokens=False
        )
        steering_start_pos = tokens_before_trial['input_ids'].shape[1] - 1
    else:
        raise ValueError(f"Could not find '{trial_text}' in prompt")

    return steering_start_pos


