def load_per_concept_detection_rates(
    exp21_path: Path = None,
    trials: Optional[List[int]] = None,
) -> Dict[str, float]:
    """Load per-concept detection rates from exp21 data.

    Returns dict mapping concept name -> detection rate (0 to 1).
    """
    if exp21_path is None:
        exp21_path = EXP21_DATA_PATH

    if not exp21_path.exists():
        print(f"Warning: exp21 data not found at {exp21_path}")
        return {}

    with open(exp21_path) as f:
        data = json.load(f)

    results = data['results']

    if trials is not None:
        trials_set = set(trials)
        results = [r for r in results if r.get('trial') in trials_set]
        print(f"  Filtered to trials {min(trials)}-{max(trials)}: {len(results)} results")

    concept_stats = defaultdict(lambda: {'detected': 0, 'total': 0})
    for r in results:
        concept = r['concept']
        concept_stats[concept]['total'] += 1
        if 'evaluations' in r and 'claims_detection' in r['evaluations']:
            if r['evaluations']['claims_detection'].get('claims_detection', False):
                concept_stats[concept]['detected'] += 1
        elif r.get('detected', False):
            concept_stats[concept]['detected'] += 1

    detection_rates = {
        concept: stats['detected'] / stats['total']
        for concept, stats in concept_stats.items()
        if stats['total'] > 0
    }
    return detection_rates


