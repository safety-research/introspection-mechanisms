class MLPActivationCapture:
    """Capture MLP input activations at specified layers."""

    def __init__(self, model: ModelWrapper, target_layers: List[int]):
        self.model = model
        self.target_layers = target_layers
        self.hooks = []
        self.pre_mlp_acts = {}

    def _make_hook(self, layer_idx: int):
        def hook(module, input, output):
            if isinstance(input, tuple):
                x = input[0]
            else:
                x = input
            self.pre_mlp_acts[layer_idx] = x.detach().clone()
        return hook

    def register_hooks(self):
        self.clear()
        for layer_idx in self.target_layers:
            layer = self.model.get_layer_module(layer_idx)
            if hasattr(layer, 'mlp'):
                mlp = layer.mlp
            elif hasattr(layer, 'feed_forward'):
                mlp = layer.feed_forward
            else:
                raise ValueError(f"Could not find MLP in layer {layer_idx}")
            h = mlp.register_forward_hook(self._make_hook(layer_idx))
            self.hooks.append(h)

    def remove_hooks(self):
        for h in self.hooks:
            h.remove()
        self.hooks = []

    def clear(self):
        self.pre_mlp_acts = {}


