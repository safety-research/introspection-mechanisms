# Cache for W_unembed mean to avoid recomputing
_unembed_mean_cache = {}


def compute_feature_to_logit_effects(
    model: ModelWrapper,
    transcoder: JumpReLUSAE,
    activations: torch.Tensor,
    layer: int,
    position: int,
    target_token_ids: List[int],
) -> Dict[int, float]:
    """Compute aggregated effect of each feature on target logits (batched)."""
    global _unembed_mean_cache

    with torch.no_grad():
        x = activations[0, position]
        feature_acts = transcoder.encode(x.unsqueeze(0)).squeeze(0)

        W_unembed = model.model.lm_head.weight
        W_dec = transcoder.w_dec

        cache_key = id(W_unembed)
        if cache_key not in _unembed_mean_cache:
            _unembed_mean_cache[cache_key] = W_unembed.mean(dim=0)
        unembed_mean = _unembed_mean_cache[cache_key]

        unembed_vecs = W_unembed[target_token_ids]
        unembed_demeaned = unembed_vecs - unembed_mean.unsqueeze(0)
        decoder_to_logits = W_dec @ unembed_demeaned.T.to(W_dec.dtype)
        total_effects = decoder_to_logits.sum(dim=1) * feature_acts

        active_mask = feature_acts > 0
        active_indices = active_mask.nonzero(as_tuple=True)[0]
        effects_dict = {int(i): total_effects[i].item() for i in active_indices}

        return effects_dict


def collect_activations_for_condition(
    model: ModelWrapper,
    prompt: str,
    steering_vector: Optional[torch.Tensor],
    steering_strength: float,
    layers: List[int],
    steering_start_pos: Optional[int] = None,
) -> Tuple[Dict[int, torch.Tensor], torch.Tensor]:
    """Run model and collect MLP input activations at specified layers."""
    inputs = model.tokenizer(prompt, return_tensors="pt", add_special_tokens=False).to(DEVICE)
    capture = MLPActivationCapture(model, layers)
    capture.register_hooks()

    if steering_vector is not None:
        steering_hook = SteeringHook(
            layer_idx=STEERING_LAYER,
            steering_vector=steering_vector,
            strength=steering_strength,
            start_pos=steering_start_pos,
            model_type="gemma",
        )
        steering_hook.register(model)

    with torch.no_grad():
        outputs = model.model(**inputs)
        logits = outputs.logits

    activations = {layer: acts.clone() for layer, acts in capture.pre_mlp_acts.items()}
    capture.remove_hooks()

    if steering_vector is not None:
        steering_hook.remove()

    return activations, logits


