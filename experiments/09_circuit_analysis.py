def create_discriminative_features_plot(
    features: List[FeatureNode],
    raw_stats: Dict[Tuple[int, int], Dict],
    feature_labels: Dict[int, Dict[int, str]],
    title: str,
    save_path: Path,
    top_k: int = 50,
):
    """Create a plot showing features that discriminate SUCCESS from FAILURE.

    Two rows:
    - Top row: SUCCESS-aligned features (positive Cohen's d)
    - Bottom row: FAILURE-aligned features (negative Cohen's d)
    """
    if not raw_stats:
        print(f"Warning: No discriminative features to plot")
        return

    all_features_sorted = sorted(
        raw_stats.items(),
        key=lambda x: x[1].get('cohens_d', 0),
        reverse=True
    )

    success_aligned = [(k, v) for k, v in all_features_sorted if v.get('cohens_d', 0) > 0][:top_k]
    failure_aligned = [(k, v) for k, v in all_features_sorted if v.get('cohens_d', 0) < 0]
    failure_aligned = sorted(failure_aligned, key=lambda x: x[1].get('cohens_d', 0))[:top_k]

    fig, axes = plt.subplots(2, 3, figsize=(36, 28))

    def plot_row(row_axes, feature_list, row_title, color_positive, color_negative):
        if not feature_list:
            for ax in row_axes:
                ax.text(0.5, 0.5, 'No features found', ha='center', va='center', fontsize=14)
                ax.set_title(row_title, fontsize=14, fontweight='bold')
            return

        labels = []
        cohens_d_vals = []
        mean_diffs = []
        consistencies = []

        for (layer, feat_idx), stats in feature_list:
            label_text = feature_labels.get(layer, {}).get(feat_idx, "")
            if label_text:
                label = f"L{layer}F{feat_idx}: {label_text}"
            else:
                label = f"L{layer}F{feat_idx}"
            labels.append(label)
            cohens_d_vals.append(stats.get('cohens_d', 0))
            mean_diffs.append(stats.get('mean_diff', 0))
            consistencies.append(stats.get('consistency', 0))

        labels = labels[::-1]
        cohens_d_vals = cohens_d_vals[::-1]
        mean_diffs = mean_diffs[::-1]
        consistencies = consistencies[::-1]
        y_pos = np.arange(len(labels))

        # Cohen's d
        ax1 = row_axes[0]
        colors = [color_positive if x > 0 else color_negative for x in cohens_d_vals]
        ax1.barh(y_pos, cohens_d_vals, color=colors, alpha=0.8, height=0.7)
        ax1.set_yticks(y_pos)
        ax1.set_yticklabels(labels, fontsize=10, fontfamily='monospace')
        ax1.set_xlabel("Cohen's d (effect size)", fontsize=12)
        ax1.set_title(f"{row_title}: Cohen's d", fontsize=12, fontweight='bold')
        ax1.axvline(x=0, color='black', linewidth=0.5)
        ax1.grid(axis='x', alpha=0.3)

        # Mean difference
        ax2 = row_axes[1]
        colors2 = [color_positive if x > 0 else color_negative for x in mean_diffs]
        ax2.barh(y_pos, mean_diffs, color=colors2, alpha=0.8, height=0.7)
        ax2.set_yticks(y_pos)
        ax2.set_yticklabels(labels, fontsize=10, fontfamily='monospace')
        ax2.set_xlabel('Mean Difference (SUCCESS - FAILURE)', fontsize=12)
        ax2.set_title(f"{row_title}: Mean Difference", fontsize=12, fontweight='bold')
        ax2.axvline(x=0, color='black', linewidth=0.5)
        ax2.grid(axis='x', alpha=0.3)

        # Consistency
        ax3 = row_axes[2]
        ax3.barh(y_pos, consistencies, color='steelblue', alpha=0.8, height=0.7)
        ax3.set_yticks(y_pos)
        ax3.set_yticklabels(labels, fontsize=10, fontfamily='monospace')
        ax3.set_xlabel('Consistency (fraction of concepts)', fontsize=12)
        ax3.set_title(f"{row_title}: Consistency", fontsize=12, fontweight='bold')
        ax3.set_xlim(0, 1.05)
        ax3.axvline(x=0.5, color='gray', linewidth=0.5, linestyle='--')
        ax3.grid(axis='x', alpha=0.3)

    plot_row(axes[0], success_aligned, f"SUCCESS-aligned (top {len(success_aligned)})", '#28a745', '#dc3545')
    plot_row(axes[1], failure_aligned, f"FAILURE-aligned (top {len(failure_aligned)})", '#28a745', '#dc3545')

    fig.suptitle(title, fontsize=16, fontweight='bold', y=0.995)
    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight', facecolor='white')
    print(f"Saved: {save_path}")
    plt.close()


