class TranscoderIntervention:
    """Hook that modifies transcoder feature activations."""

    def __init__(self, model: ModelWrapper, transcoder: JumpReLUSAE,
                 layer: int, interventions: Dict[int, float], position: int = -1):
        self.model = model
        self.transcoder = transcoder
        self.layer = layer
        self.interventions = interventions
        self.position = position
        self.hook = None

    def _intervention_hook(self, module, input, output):
        if isinstance(input, tuple):
            x = input[0]
        else:
            x = input

        pos = self.position if self.position >= 0 else x.shape[1] + self.position
        x_pos = x[0, pos]

        with torch.no_grad():
            features = self.transcoder.encode(x_pos.unsqueeze(0)).squeeze(0)

        modified_features = features.clone()
        for feat_idx, scale in self.interventions.items():
            modified_features[feat_idx] = features[feat_idx] * scale

        original_output = self.transcoder.decode(features.unsqueeze(0)).squeeze(0)
        modified_output = self.transcoder.decode(modified_features.unsqueeze(0)).squeeze(0)
        delta = modified_output - original_output

        if isinstance(output, tuple):
            modified = list(output)
            hidden = modified[0].clone()
            hidden[0, pos] = hidden[0, pos] + delta.to(hidden.dtype)
            modified[0] = hidden
            return tuple(modified)
        else:
            output_modified = output.clone()
            output_modified[0, pos] = output_modified[0, pos] + delta.to(output.dtype)
            return output_modified

    def register(self):
        layer_module = self.model.get_layer_module(self.layer)
        if hasattr(layer_module, 'mlp'):
            mlp = layer_module.mlp
        else:
            mlp = layer_module.feed_forward
        self.hook = mlp.register_forward_hook(self._intervention_hook)

    def remove(self):
        if self.hook:
            self.hook.remove()


