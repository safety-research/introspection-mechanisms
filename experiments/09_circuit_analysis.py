def discover_aggregated_circuit(
    model: ModelWrapper,
    transcoders: Dict[int, JumpReLUSAE],
    concept_vectors: Dict[str, torch.Tensor],
    yes_token_ids: List[int],
    no_token_ids: List[int],
    trials: List[int],
    steering_strength: float,
    condition: str,
    introspection_prompt_template: str,
    top_k_global: int = 30,
    min_effect: float = 5.0,
    steering_start_pos: Optional[int] = None,
    token_mode: str = DEFAULT_TOKEN_MODE,
    precomputed_activations: Optional[PrecomputedActivations] = None,
) -> AggregatedCircuit:
    """Discover circuit features aggregated across concepts and trials.

    Methodology:
    - Tracks ALL features, not just active ones
    - For success/failure: uses concept partition directly (no response filtering)
    - Uses tensor-based collection for proper statistics

    Args:
        token_mode: How to aggregate activations over token positions
        precomputed_activations: If provided, use these instead of collecting fresh
    """
    first_tc = next(iter(transcoders.values()))
    n_features = first_tc.w_enc.shape[1]

    print(f"  Token aggregation mode: {token_mode}")

    # Use precomputed activations if provided
    if precomputed_activations is not None:
        print(f"  Using precomputed activations ({len(precomputed_activations.concepts)} concepts)")
        concepts = precomputed_activations.concepts
        layers = precomputed_activations.layers

        layer_activations = {layer: [] for layer in layers}
        all_yes_probs = []
        all_no_probs = []

        for concept in concepts:
            logits_info = precomputed_activations.logits_info[concept]
            all_yes_probs.extend(logits_info['yes_probs'])
            all_no_probs.extend(logits_info['no_probs'])

            for layer in layers:
                if layer in precomputed_activations.activations[concept]:
                    trial_acts = precomputed_activations.activations[concept][layer]
                    for i in range(trial_acts.shape[0]):
                        layer_activations[layer].append(trial_acts[i])

        n_samples = len(concepts) * len(precomputed_activations.trials)
        print(f"  Extracted {n_samples} samples from precomputed data")

    else:
        # Collect activations directly
        layer_activations = {layer: [] for layer in transcoders.keys()}
        all_yes_probs = []
        all_no_probs = []
        n_samples = 0
        concepts = list(concept_vectors.keys())

        for concept in tqdm(concepts, desc=f"Collecting {condition} activations"):
            steering_vector = concept_vectors[concept]

            for trial in trials:
                prompt = introspection_prompt_template.format(trial_num=trial)
                use_steering = (condition != 'control')

                activations, logits = collect_activations_for_condition(
                    model, prompt,
                    steering_vector if use_steering else None,
                    steering_strength,
                    list(transcoders.keys()),
                    steering_start_pos=steering_start_pos,
                )

                probs = F.softmax(logits[0, -1], dim=-1)
                yes_prob = sum(probs[tid].item() for tid in yes_token_ids)
                no_prob = sum(probs[tid].item() for tid in no_token_ids)

                all_yes_probs.append(yes_prob)
                all_no_probs.append(no_prob)
                n_samples += 1

                for layer, tc in transcoders.items():
                    if layer not in activations:
                        continue
                    if token_mode == "last_token":
                        x = activations[layer][0, -1]
                    elif token_mode == "mean_steering_tokens":
                        x_all = activations[layer][0, steering_start_pos:]
                        x = x_all.mean(dim=0)
                    else:
                        raise ValueError(f"Unknown token_mode: {token_mode}")
                    with torch.no_grad():
                        feature_acts = tc.encode(x.unsqueeze(0)).squeeze(0)
                        layer_activations[layer].append(feature_acts.cpu())

            torch.cuda.empty_cache()

        print(f"Collected {n_samples} samples for condition '{condition}'")

    if n_samples == 0:
        return AggregatedCircuit(condition=condition)

    # Convert to tensors and compute statistics
    aggregated_features = []

    for layer in sorted(transcoders.keys()):
        if not layer_activations[layer]:
            continue

        acts_tensor = torch.stack(layer_activations[layer]).float()
        mean_acts = acts_tensor.mean(dim=0)
        std_acts = acts_tensor.std(dim=0)

        top_indices = mean_acts.argsort(descending=True)[:100]

        for idx in top_indices:
            idx = int(idx)
            if mean_acts[idx] < 0.1:
                continue
            aggregated_features.append(FeatureNode(
                layer=layer,
                feature_idx=idx,
                activation_mean=float(mean_acts[idx]),
                activation_std=float(std_acts[idx]),
                differential_effect=float(mean_acts[idx]),
                n_samples=n_samples,
            ))

    aggregated_features.sort(key=lambda x: x.activation_mean, reverse=True)
    top_features = aggregated_features[:top_k_global]

    return AggregatedCircuit(
        condition=condition,
        features=top_features,
        n_concepts=len(concepts),
        n_trials=len(trials),
        mean_yes_prob=np.mean(all_yes_probs) if all_yes_probs else 0,
        mean_no_prob=np.mean(all_no_probs) if all_no_probs else 0,
    )


def discover_discriminative_features(
    model: ModelWrapper,
    transcoders: Dict[int, JumpReLUSAE],
    success_vectors: List[torch.Tensor],
    failure_vectors: List[torch.Tensor],
    yes_token_ids: List[int],
    no_token_ids: List[int],
    trials: List[int],
    steering_strength: float,
    introspection_prompt_template: str,
    top_k: int = 30,
    min_samples: int = 3,
    steering_start_pos: Optional[int] = None,
    cache_dir: Optional[Path] = None,
    device: str = 'cuda',
    token_mode: str = DEFAULT_TOKEN_MODE,
    precomputed_success: Optional[PrecomputedActivations] = None,
    precomputed_failure: Optional[PrecomputedActivations] = None,
) -> Tuple[List[FeatureNode], Dict[Tuple[int, int], Dict], Dict[int, List]]:
    """Find features that best discriminate SUCCESS from FAILURE conditions.

    Methodology:
    1. Collect ALL feature activations for ALL concepts (not just active ones)
    2. Use Cohen's d as the primary ranking metric for effect size
    3. Compute consistency: percentage of concepts where feature shows increase

    Args:
        precomputed_success: Pre-collected activations for SUCCESS concepts.
        precomputed_failure: Pre-collected activations for FAILURE concepts.

    Returns:
        - List of top discriminative features
        - Raw feature stats dict
        - Per-layer top features dict
    """
    first_tc = next(iter(transcoders.values()))
    n_features = first_tc.w_enc.shape[1]
    layers = list(transcoders.keys())

    print(f"Transcoder width: {n_features} features per layer")
    print(f"Token aggregation mode: {token_mode}")

    def precomputed_to_averaged(precomputed: PrecomputedActivations) -> Dict[int, torch.Tensor]:
        """Convert pre-collected per-trial activations to per-concept averages.

        Returns: {layer: (n_concepts, n_features)} with trials averaged
        """
        layer_activations = {layer: [] for layer in precomputed.layers}
        for concept in precomputed.concepts:
            for layer in precomputed.layers:
                if layer in precomputed.activations[concept]:
                    avg_acts = precomputed.activations[concept][layer].mean(dim=0)
                    layer_activations[layer].append(avg_acts)
        for layer in layer_activations:
            if layer_activations[layer]:
                layer_activations[layer] = torch.stack(layer_activations[layer])
            else:
                layer_activations[layer] = None
        return layer_activations

    # Use precomputed activations if provided
    if precomputed_success is not None and precomputed_failure is not None:
        print(f"\n  Using precomputed activations:")
        print(f"    SUCCESS: {len(precomputed_success.concepts)} concepts")
        print(f"    FAILURE: {len(precomputed_failure.concepts)} concepts")

        success_acts = precomputed_to_averaged(precomputed_success)
        failure_acts = precomputed_to_averaged(precomputed_failure)
        n_success = len(precomputed_success.concepts)
        n_failure = len(precomputed_failure.concepts)

    else:
        # Check cache or collect fresh
        n_success = len(success_vectors)
        n_failure = len(failure_vectors)
        cache_path = None
        if cache_dir is not None:
            cache_path = get_activation_cache_path(
                cache_dir, n_success, n_failure, steering_strength, len(trials), layers,
                token_mode=token_mode
            )

            if cache_path.exists():
                print(f"\n  Found cached activations for {token_mode}!")
                success_acts, failure_acts, metadata = load_activation_cache(cache_path, device=device)
                n_success = metadata.get('n_success', n_success)
                n_failure = metadata.get('n_failure', n_failure)
            else:
                print(f"\n  No cache found at {cache_path}, collecting activations...")
                success_acts, failure_acts = None, None
        else:
            success_acts, failure_acts = None, None

        def collect_per_concept_activations(concept_vectors_list, condition_name):
            """Collect activations, averaging trials per concept.

            Returns dict: {layer: tensor of shape (n_concepts, n_features)}
            """
            layer_activations = {layer: [] for layer in transcoders.keys()}
            n_concepts = 0

            pbar = tqdm(concept_vectors_list, desc=f"Collecting {condition_name} activations ({token_mode})")
            for concept_vec in pbar:
                concept_trial_acts = {layer: [] for layer in transcoders.keys()}
                for trial in trials:
                    prompt = introspection_prompt_template.format(trial_num=trial)
                    activations, logits = collect_activations_for_condition(
                        model, prompt, concept_vec,
                        steering_strength, list(transcoders.keys()),
                        steering_start_pos=steering_start_pos,
                    )
                    for layer, tc in transcoders.items():
                        if layer not in activations:
                            continue
                        if token_mode == "last_token":
                            x = activations[layer][0, -1]
                        elif token_mode == "mean_steering_tokens":
                            x_all = activations[layer][0, steering_start_pos:]
                            x = x_all.mean(dim=0)
                        else:
                            raise ValueError(f"Unknown token_mode: {token_mode}")
                        with torch.no_grad():
                            feature_acts = tc.encode(x.unsqueeze(0)).squeeze(0)
                            concept_trial_acts[layer].append(feature_acts.cpu())
                    torch.cuda.empty_cache()

                for layer in transcoders.keys():
                    if concept_trial_acts[layer]:
                        avg_acts = torch.stack(concept_trial_acts[layer]).mean(dim=0)
                        layer_activations[layer].append(avg_acts)
                n_concepts += 1

            for layer in layer_activations:
                if layer_activations[layer]:
                    layer_activations[layer] = torch.stack(layer_activations[layer])
                else:
                    layer_activations[layer] = None

            print(f"  Collected {n_concepts} concepts (averaged over {len(trials)} trials) for {condition_name}")
            return layer_activations, n_concepts

        # Only collect if we don't have cached data
        if success_acts is None or failure_acts is None:
            print("Collecting SUCCESS feature activations...")
            success_acts, n_success = collect_per_concept_activations(success_vectors, 'success')
            print("Collecting FAILURE feature activations...")
            failure_acts, n_failure = collect_per_concept_activations(failure_vectors, 'failure')

            if cache_path is not None:
                metadata = {
                    'n_success': n_success,
                    'n_failure': n_failure,
                    'steering_strength': steering_strength,
                    'n_trials': len(trials),
                    'layers': layers,
                    'n_features': n_features,
                    'token_mode': token_mode,
                }
                save_activation_cache(cache_path, success_acts, failure_acts, metadata)

    if n_success == 0 or n_failure == 0:
        print(f"Warning: Not enough samples (success={n_success}, failure={n_failure})")
        return [], {}, {}

    # Compute discriminative statistics per layer using Cohen's d
    discriminative_features = []
    raw_stats = {}
    per_layer_top = {}

    for layer in sorted(transcoders.keys()):
        success_stack = success_acts.get(layer)
        failure_stack = failure_acts.get(layer)
        if success_stack is None or failure_stack is None:
            continue

        success_stack = success_stack.float()
        failure_stack = failure_stack.float()
        n_success_samples = success_stack.shape[0]
        n_failure_samples = failure_stack.shape[0]

        print(f"  L{layer}: {n_success_samples} success samples, {n_failure_samples} failure samples")

        mean_success = success_stack.mean(dim=0)
        mean_failure = failure_stack.mean(dim=0)
        mean_diff = mean_success - mean_failure

        var_success = success_stack.var(dim=0, unbiased=True)
        var_failure = failure_stack.var(dim=0, unbiased=True)

        pooled_std = torch.sqrt(
            ((n_success_samples - 1) * var_success + (n_failure_samples - 1) * var_failure) /
            (n_success_samples + n_failure_samples - 2 + 1e-8)
        )
        cohens_d = mean_diff / (pooled_std + 1e-8)

        consistency_success = (success_stack > mean_failure.unsqueeze(0) + 0.1).float().mean(dim=0)
        consistency_failure = (failure_stack > mean_success.unsqueeze(0) + 0.1).float().mean(dim=0)

        def add_to_raw_stats(idx, direction, consistency_val):
            idx = int(idx)
            key = (layer, idx)
            if key not in raw_stats:
                raw_stats[key] = {
                    'mean_success': float(mean_success[idx]),
                    'mean_failure': float(mean_failure[idx]),
                    'mean_diff': float(mean_diff[idx]),
                    'cohens_d': float(cohens_d[idx]),
                    'pooled_std': float(pooled_std[idx]),
                    'consistency': float(consistency_val[idx]),
                    'direction': direction,
                    'n_success_samples': n_success_samples,
                    'n_failure_samples': n_failure_samples,
                }

        # Top features by Cohen's d (success-aligned)
        sorted_by_cohens_d = cohens_d.argsort(descending=True)
        top_cohens_d_success = sorted_by_cohens_d[:50]
        for idx in top_cohens_d_success:
            add_to_raw_stats(idx, 'success_aligned', consistency_success)

        # Top features by Cohen's d (failure-aligned)
        sorted_by_cohens_d_neg = cohens_d.argsort(descending=False)
        top_cohens_d_failure = sorted_by_cohens_d_neg[:50]
        for idx in top_cohens_d_failure:
            add_to_raw_stats(idx, 'failure_aligned', consistency_failure)

        # Top features by mean_diff (both directions)
        sorted_by_mean_diff = mean_diff.argsort(descending=True)
        for idx in sorted_by_mean_diff[:50]:
            add_to_raw_stats(idx, 'success_aligned', consistency_success)
        sorted_by_mean_diff_neg = mean_diff.argsort(descending=False)
        for idx in sorted_by_mean_diff_neg[:50]:
            add_to_raw_stats(idx, 'failure_aligned', consistency_failure)

        # Build per-layer features from Cohen's d ranking
        layer_features = []
        for rank, idx in enumerate(top_cohens_d_success):
            idx = int(idx)
            node = FeatureNode(
                layer=layer,
                feature_idx=idx,
                activation_mean=float(mean_diff[idx]),
                activation_std=float(pooled_std[idx]),
                effect_on_yes=float(cohens_d[idx]),
                differential_effect=float(cohens_d[idx]),
                n_samples=n_success_samples + n_failure_samples,
            )
            layer_features.append(node)
            if rank < top_k:
                discriminative_features.append(node)
        per_layer_top[layer] = layer_features[:10]

    # Sort all features by Cohen's d
    discriminative_features.sort(key=lambda x: x.differential_effect, reverse=True)
    top_features = discriminative_features[:top_k]

    print(f"\nFound {len(discriminative_features)} discriminative features, keeping top {top_k}")
    n_success_aligned = sum(1 for f in top_features if f.differential_effect > 0)
    n_failure_aligned = sum(1 for f in top_features if f.differential_effect < 0)
    print(f"  {n_success_aligned} success-aligned, {n_failure_aligned} failure-aligned")

    return top_features, raw_stats, per_layer_top


