@dataclass
class PrecomputedActivations:
    """
    Stores pre-collected feature activations for efficient reuse.

    Activations are stored per-concept, per-trial, per-layer.
    Structure:
        activations: {concept: {layer: tensor of shape (n_trials, n_features)}}
        logits_info: {concept: {'yes_probs': [...], 'no_probs': [...]}}
    """
    activations: Dict[str, Dict[int, torch.Tensor]]
    logits_info: Dict[str, Dict[str, List[float]]]
    concepts: List[str]
    trials: List[int]
    with_steering: bool
    steering_strength: float
    token_mode: str
    n_features: int
    layers: List[int]


def collect_activations_batch(
    model: ModelWrapper,
    transcoders: Dict[int, JumpReLUSAE],
    concept_vectors: Dict[str, torch.Tensor],
    yes_token_ids: List[int],
    no_token_ids: List[int],
    trials: List[int],
    steering_strength: float,
    introspection_prompt_template: str,
    with_steering: bool,
    steering_start_pos: Optional[int] = None,
    token_mode: str = "last_token",
) -> PrecomputedActivations:
    """
    Collect feature activations for ALL concepts in a single pass.

    This function eliminates redundancy by collecting once and allowing
    reuse for different analyses (aggregated circuit, discriminative features).

    Args:
        with_steering: If True, apply concept steering vectors. If False, no steering.
        token_mode: "last_token" or "mean_steering_tokens"

    Returns:
        PrecomputedActivations with per-concept, per-trial data
    """
    first_tc = next(iter(transcoders.values()))
    n_features = first_tc.w_enc.shape[1]
    layers = list(transcoders.keys())

    condition_name = "steered" if with_steering else "control"
    print(f"\n  Collecting {condition_name} activations for {len(concept_vectors)} concepts")
    print(f"  Token mode: {token_mode}, Trials: {len(trials)}")

    all_activations = {}
    all_logits_info = {}
    concepts = list(concept_vectors.keys())

    for concept in tqdm(concepts, desc=f"Collecting {condition_name} activations"):
        steering_vector = concept_vectors[concept] if with_steering else None
        concept_trial_acts = {layer: [] for layer in layers}
        concept_yes_probs = []
        concept_no_probs = []

        for trial in trials:
            prompt = introspection_prompt_template.format(trial_num=trial)
            activations, logits = collect_activations_for_condition(
                model, prompt,
                steering_vector,
                steering_strength,
                layers,
                steering_start_pos=steering_start_pos,
            )

            probs = F.softmax(logits[0, -1], dim=-1)
            yes_prob = sum(probs[tid].item() for tid in yes_token_ids)
            no_prob = sum(probs[tid].item() for tid in no_token_ids)
            concept_yes_probs.append(yes_prob)
            concept_no_probs.append(no_prob)

            for layer, tc in transcoders.items():
                if layer not in activations:
                    continue
                if token_mode == "last_token":
                    x = activations[layer][0, -1]
                elif token_mode == "mean_steering_tokens":
                    x_all = activations[layer][0, steering_start_pos:]
                    x = x_all.mean(dim=0)
                else:
                    raise ValueError(f"Unknown token_mode: {token_mode}")
                with torch.no_grad():
                    feature_acts = tc.encode(x.unsqueeze(0)).squeeze(0)
                    concept_trial_acts[layer].append(feature_acts.cpu())

        all_activations[concept] = {
            layer: torch.stack(concept_trial_acts[layer])
            for layer in layers if concept_trial_acts[layer]
        }
        all_logits_info[concept] = {
            'yes_probs': concept_yes_probs,
            'no_probs': concept_no_probs,
        }
        torch.cuda.empty_cache()

    n_samples = len(concepts) * len(trials)
    print(f"  Collected {n_samples} total samples ({len(concepts)} concepts x {len(trials)} trials)")

    return PrecomputedActivations(
        activations=all_activations,
        logits_info=all_logits_info,
        concepts=concepts,
        trials=trials,
        with_steering=with_steering,
        steering_strength=steering_strength,
        token_mode=token_mode,
        n_features=n_features,
        layers=layers,
    )


def filter_precomputed_activations(
    precomputed: PrecomputedActivations,
    concepts_to_keep: List[str],
) -> PrecomputedActivations:
    """Filter pre-computed activations to a subset of concepts."""
    filtered_activations = {c: precomputed.activations[c] for c in concepts_to_keep if c in precomputed.activations}
    filtered_logits = {c: precomputed.logits_info[c] for c in concepts_to_keep if c in precomputed.logits_info}

    return PrecomputedActivations(
        activations=filtered_activations,
        logits_info=filtered_logits,
        concepts=[c for c in concepts_to_keep if c in precomputed.activations],
        trials=precomputed.trials,
        with_steering=precomputed.with_steering,
        steering_strength=precomputed.steering_strength,
        token_mode=precomputed.token_mode,
        n_features=precomputed.n_features,
        layers=precomputed.layers,
    )


